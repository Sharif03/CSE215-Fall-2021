package theoryLec02;

public class JavaLiteral {

	public static void main(String[] args) {
		//Integral literal
		long longValue = 20;
		int intValue = 10;
		short shortValue = 6;
		byte byteValue = 2;
		
		//Floating-Point literal
		float floatValue = 3.5f;
		double doubleValue = 3.9d;
		
		// boolean literal;
		boolean boolValue = false;
		
		// charactar literal;
		char charValue = 'A';
		
		// String literal
		String stringValue = "Hello World";

	}

}
